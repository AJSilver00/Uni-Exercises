# Repeating strings


theString = input ("Please enter a character string")
num = input ("Please enter in a positive whole number")
num = int(num)                  #This step is converting the string num into integer and then storing the result back into num.
multiString = (theString * num) #This will repeat theString num times. Like so:
print (multiString)             #will print it all in the same line: e.g. Hello Hello Hello


# The * is used to repeat a string an integer number of times.
# E.g. Hello * 3
# Will print : Hello Hello Hello
