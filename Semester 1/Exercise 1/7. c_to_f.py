# Celsius to Fahrenheit

celsius = input ("Please enter a temperature in degrees Celsius")
celsius = float(celsius)
fahrenheit = celsius * 9 / 5 + 32
print (celsius, "celsius converted to Fahrenheit, is", fahrenheit)

#The function float() is used to convert a string representation of a number
# into a float number.
# The formula F = C x 9 / 5 + 32 is used to calculate Fahrenheit.
